def main():
    n = 42
    print("Decimal:", n)
    print("Binary :", bin(n))   # 0b101010
    print("Octal  :", oct(n))   # 0o52
    print("Hex    :", hex(n))   # 0x2a

    # Direct literals
    print("Octal literal 0o52:", 0o52)
    print("Hex literal 0x2A  :", 0x2A)

if __name__ == "__main__":
    main()
